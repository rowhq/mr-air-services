export function BlocksSidebar() {
  const { draftBlocks, addBlock } = useEditorStore();

  const categories = [...new Set(blockDefinitions.map((b) => b.category))];

  const handleAddBlock = (type: BlockType) => {
    addBlock(type, draftBlocks.length);
  };

  return (
    <div className="w-64 bg-white border-r border-gray-200 flex flex-col h-full">
      <div className="p-4 border-b border-gray-200">
        <h2 className="font-semibold text-gray-900">Add Blocks</h2>
        <p className="text-xs text-gray-500 mt-1">
          Click to add at the end, or drag to canvas
        </p>
      </div>

      <div className="flex-1 overflow-y-auto p-4">
        {categories.map((category) => (
          <div key={category} className="mb-6">
            <h3 className="text-xs font-semibold text-gray-400 uppercase tracking-wider mb-3">
              {category}
            </h3>
            <div className="space-y-2">
              {blockDefinitions
                .filter((b) => b.category === category)
                .map((block) => (
                  <button
                    key={block.type}
                    onClick={() => handleAddBlock(block.type)}
                    className="w-full flex items-center gap-3 p-3 bg-gray-50 hover:bg-gray-100 rounded-lg transition-colors text-left group"
                  >
                    <div className="w-10 h-10 bg-white rounded-lg flex items-center justify-center text-gray-600 shadow-sm group-hover:text-blue-600 transition-colors">
                      {block.icon}
                    </div>
                    <span className="text-sm font-medium text-gray-700 group-hover:text-gray-900">
                      {block.label}
                    </span>
                  </button>
                ))}
            </div>
          </div>
        ))}
      </div>
    </div>
  );
}
